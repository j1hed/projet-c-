void gestion_activite::on_statistique_clicked()
{


        QSqlQueryModel * model = new QSqlQueryModel();
        model->setQuery("SELECT * FROM activite WHERE TYPE='Peinture'");
        float dispo1 = model->rowCount();

        model->setQuery("SELECT * FROM activite WHERE TYPE='Musique'");
        float dispo = model->rowCount();

        model->setQuery("SELECT * FROM activite WHERE TYPE='Theatre'");
        float dispo2 = model->rowCount();

        float total = dispo1 + dispo+dispo2;
        QString a = QString("Peinture " + QString::number((dispo1 * 100) / total, 'f', 2) + "%");
        QString b = QString("Musique  " + QString::number((dispo * 100) / total, 'f', 2) + "%");
        QString c = QString("Theatre  " + QString::number((dispo2 * 100) / total, 'f', 2) + "%");


        QPieSeries *series = new QPieSeries();
        series->append(a, dispo1);
        series->append(b, dispo);
        series->append(c, dispo2);

        if (dispo1 != 0)
        {
            QPieSlice *slice = series->slices().at(0);
            slice->setLabelVisible();
            slice->setPen(QPen());
        }

        if (dispo != 0)
        {
            QPieSlice *slice1 = series->slices().at(1);
            slice1->setLabelVisible();
        }
        if (dispo2 != 0)
        {
            QPieSlice *slice2 = series->slices().at(2);
            slice2->setLabelVisible();
        }


        QChart *chart = new QChart();
        chart->addSeries(series);
        chart->setTitle("TYPE D'ACTIVITE" + QString::number(total));

        QChartView *chartView = new QChartView(chart);
        chartView->setRenderHint(QPainter::Antialiasing);
        chartView->resize(1000, 500);
        chartView->show();


        }



