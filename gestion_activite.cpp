void gestion_activite::on_qrcodegen_clicked()
    {
        using namespace qrcodegen;


                    QTableView tableView;
                    QSqlQueryModel * Mod=new  QSqlQueryModel();
                    QString value=ui->lineEdit_qrcode->text();


                         QSqlQuery qry;




                         qry.prepare("select * from gestion_activite where id='"+value+"'");
                         qry.exec();
                         Mod->setQuery(qry);
                         tableView.setModel(Mod);



                        QString id = tableView.model()->data(tableView.model()->index(0, 0)).toString().simplified();
                        QString titre= tableView.model()->data(tableView.model()->index(0, 1)).toString().simplified();
                        QString type = tableView.model()->data(tableView.model()->index(0, 2)).toString().simplified();
                        QString date_act = tableView.model()->data(tableView.model()->index(0, 3)).toString().simplified();
                        QString nbplaces = tableView.model()->data(tableView.model()->index(0, 4)).toString().simplified();




                    QString text = "ID"+id+"\n"+"titre  "+titre+"\n"+"type  "+type+"\n"+" date_act  "+date_act+"\n"+"nbplaces  "+nbplaces;
                      // Create the QR Code object
                      QrCode qr = QrCode::encodeText( text.toUtf8().data(), QrCode::Ecc::MEDIUM );

                      qint32 sz = qr.getSize();
                      QImage im(sz,sz, QImage::Format_RGB32);
                        QRgb black = qRgb( 191,112,105 );
                        QRgb white = qRgb(255,255,255);
                      for (int y = 0; y < sz; y++)
                        for (int x = 0; x < sz; x++)
                          im.setPixel(x,y,qr.getModule(x, y) ? black : white );//setpixelmap tafichilek qr code
                      ui->qrcodecommande->setPixmap( QPixmap::fromImage(im.scaled(200,200,Qt::KeepAspectRatio,Qt::FastTransformation),Qt::MonoOnly) );


    }

